
public class ex1 {

	public static void main(String[] args) {
		int var1=6;
		int var2=4;

		System.out.println(var1 + " + " + var2 + " = " + (var1+var2));
		System.out.println(var1 + " - " + var2 + " = " + (var1-var2));
		System.out.println(var1 + " * " + var2 + " = " + (var1*var2));
		System.out.println(var1 + " / " + var2 + " = " + (var1/var2));
		System.out.println(var1 + " % " + var2 + " = " + (var1%var2));
	}

}
